# Number of chars to split by
n = 300

@hook.command
def diss(conn, nick, chan, text):
    """[--yomomma] [--gamer] [--shakespeare] [--nerd] [--celebrity] [--movie] [--generated] <target> -- Insult command. Named diss for lack of a better name. If no option is specified, a random one is selected."""
    if text:
        if text.partition(" ")[0].startswith("--") == True:
            text = text.split(" ")
            option = text[0].lower()
            if option == "--yomomma":
                insult_type = 1
            elif option == "--gamer":
                insult_type = 2
            elif option == "--shakesphere":
                insult_type = 3
            elif option == "--nerd":
                insult_type = 4
            elif option == "--celebrity":
                insult_type = 5
            elif option == "--movie":
                insult_type = 6
            elif option == "--generated":
                insult_type = 7
            else:
                insult_type = random.randint(1, 7)
            try:
                targetnick = text[1]
            except IndexError:
                targetnick = None
        else:
            targetnick = text.partition(" ")[0]
            insult_type = random.randint(1, 7)
    else:
        targetnick = None
        insult_type = random.randint(1, 7)
    if insult_type == 1:
        insult_list = yomomma
    elif insult_type == 2:
        insult_list = gamer
    elif insult_type == 3:
        insult_list = shakesphere
    elif insult_type == 4:
        insult_list = nerd
    elif insult_type == 5:
        insult_list = celebrity
    elif insult_type == 6:
        insult_list = movie
    elif insult_type == 7:
        insult_list = generated
    insult_choice = str(random.choice(insult_list))
    insult_choice = "".join(insult_choice.split("\n"))
    if targetnick != None:
        reply = targetnick+": "+insult_choice
        reply = textwrap.wrap(reply, n, break_long_words=False)
        for i in reply:
            conn.cmd("PRIVMSG " + chan + " :"+i)
    else:
        targetnick = nick
        reply = targetnick+": "+insult_choice
        reply = textwrap.wrap(reply, n, break_long_words=False)
        for i in reply:
            conn.cmd("PRIVMSG " + chan + " :"+i)
